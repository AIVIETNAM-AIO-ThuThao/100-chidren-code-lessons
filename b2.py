#2. Bài 2. Phép toán số học. Nhập vào 2 số nguyên dương a và b. 
        #Tính tổng, hiệu, tích thương của 2 số đó và in kết quả ra màn hình.
a = int(input("Nhập vào số nguyên dương thứ nhất: "))
b = int(input("Nhập vào số nguyên dương thứ 2: "))
print("Tổng của 2 số là: ", a + b)
print("Hiệu của 2 số là: ", a - b)
print("Tich của 2 số là: ", a * b)
print("Thương của 2 số là: ", a  / b)